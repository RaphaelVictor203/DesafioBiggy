
public class ProdutoCompra {

	private int idProd;
	private int qntd;
	
	public ProdutoCompra() {
		this.idProd = 0;
		this.qntd = 0;
	}

	public int getIdProd() {
		return idProd;
	}
	public void setIdProd(int idProd) {
		this.idProd = idProd;
	}
	public int getQntd() {
		return qntd;
	}
	public void setQntd(int qntd) {
		this.qntd = qntd;
	}
	
}
